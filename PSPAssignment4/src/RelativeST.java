
public class RelativeST {
	private double xi; //loc
	private double ln;
	private double lnt;
	
	public RelativeST(double x, double l, double t)
	{
		xi = x;
		ln = l;
		lnt = t;
	}
	
	public double getxi()
	{
		return xi;
	}
	
	public double getln()
	{
		return ln;
	}
	
	public double getlnt()
	{
		return lnt;
	}
	
}
